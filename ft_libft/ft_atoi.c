int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	r;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
		break ;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		 r *= 10;
		 r = r + (str[i] - '0');
		 i++;
	}
	return (r * s);
}
